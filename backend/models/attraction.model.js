import mongoose from "mongoose";
const attractionSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);
const Attraction = mongoose.model("Attraction", attractionSchema);
export default Attraction;